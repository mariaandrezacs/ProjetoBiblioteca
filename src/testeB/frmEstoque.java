    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int id = Integer.valueOf(tabEstoque.getValueAt(tabEstoque.getSelectedRow(), 0).toString());
                        
        dbEstoque db = new dbEstoque();
        estoque rem = db.ConsultaId(id);
        
        frmInfoEstoque frm = new frmInfoEstoque();
        frm.lblId.setText(String.valueOf((rem.getId())));
        frm.txtAutor.setText(rem.getAutor());
        frm.txtTitulo.setText(rem.getTitulo());
        frm.txtEditora.setText(rem.getEditora());
        frm.txtAnoPublicacao.setText(String.valueOf(rem.getAno_publicacao()));
        frm.txtQuantidade.setText(String.valueOf((rem.getQuantidade())));
        
        frm.setVisible(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

