    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resp = JOptionPane.showConfirmDialog(this, "Deseja excluir o registro selecionado?", "Exclusao de registro", JOptionPane.YES_NO_OPTION);
                
        
        if(resp == JOptionPane.YES_OPTION){
            int id = Integer.valueOf(tabEstoque.getValueAt(tabEstoque.getSelectedRow(), 0).toString());
                        
            dbEstoque db = new dbEstoque();
            estoque rem = db.ConsultaId(id);
            
            db.excluir(rem);
             
            LeGridEstoque();}
    }//GEN-LAST:event_btnExcluirActionPerformed

