    public void LeGridEstoque(){  
        ArrayList<estoque> listaEstoque = new ArrayList<estoque>();
        dbEstoque db = new dbEstoque();
        
        listaEstoque = db.listar("");
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Id");
        modelo.addColumn("Autor");
        modelo.addColumn("Titulo");
        modelo.addColumn("Editora");
        modelo.addColumn("Ano Publicação");
        modelo.addColumn("Quantidade");

        estoque es;
        
        Iterator ite = listaEstoque.iterator();
        
        while(ite.hasNext()){
            es = (estoque)ite.next();
            
            Object[] ob = {es.getId(), es.getAutor(), es.getTitulo(), es.getEditora(), es.getAno_publicacao(), es.getQuantidade()};
            modelo.addRow(ob);
        }
        
        tabEstoque.setModel(modelo);
    }

